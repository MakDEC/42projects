#include "libft.h"

void	ft_rot13(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			str[i] += 13;
			if (str[i] > 126)
				str[i] -= 94;
		}
		i++;
	}
}
